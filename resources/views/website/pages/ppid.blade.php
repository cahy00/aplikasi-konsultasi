							<p style="text-align: justify">
								Keterbukaan Informasi Publik menjadi aspek penting dalam rangka mewujudkan penyelenggaraan sistem pemerintahan yang transparan (good governance). Oleh karena itu, hak publik untuk memperoleh Informasi sesuai dengan peraturan perundangÂ¬undangan relevan guna meningkatkan keterlibatan masyarakat dalam proses pengambilan keputusan publik. Dengan adanya akses informasi, masyarakat dapat lebih aktif dalam mengawasi jalannya pemerintahan dan memberikan masukan terhadap kebijakan publik.
							</p>
							<p style="text-align: justify">
								Guna memberikan jaminan kepada warga Negara terkait keterbukaan informasi publik dan mewujudkan penyelenggaraan pemerintahan yang transparan, efektif, dan efisien, pemerintah menetapkan Undang-Undang Nomor 14 Tahun 2008 tentang Keterbukaan Informasi Publik (UU KIP). Selanjutnya di tahun 2010, pemerintah menetapkan Peraturan Pemerintah Nomor 61 tentang Pelaksanaan Undang-Undang Nomor 14 Tahun 2008 tentang Keterbukaan Informasi Publik, diikuti dengan Peraturan Komisi Informasi Nomor 1 tahun 2010 tentang Standar Layanan Informasi Publik.
