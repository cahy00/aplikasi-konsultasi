						<h2 class="display-4" style="text-align: center">VISI dan MISI</h2>
						<h2>VISI</h2>
						<p style="text-align: justify">
							Mewujudkan pelayanan informasi publik di Badan Kepegawaian Negara yang akuntabel, efisien, dan efektif.
						</p>
						<br>
						<h2>MISI</h2>
						<p style="text-align: justify">
							<ol type="1">
								<li>Pembinaan penyelenggaraan manajemen ASN</li>
								<li>Membangun dan mengembangkan sistem pelayanan informasi dan dokumentasi yang berkualitas dan modern.
								</li>
								<li>Meningkatkan kualitas pelayanan melalui peningkatan kompetensi dan profesionalisme sumberdaya manusia .
								</li>
								<li>Memperkuat jaringan pelayanan informasi dan dokumentasi di bidang kepegawaian ASN.
								</li>
							</ll>
						</p>
		
						<h3 class="display-4" style="text-align: center">TUGAS, FUNGSI, dan WEWENANG</h2>
						<h2>TUGAS</h2>
						<P>PPID Badan Kepegawaian Negara bertugas:</P>
						<p style="text-align: justify">
							<ol type="1">
								<li>menyediakan dan mengamankan Informasi Publik;</li>
								<li>memberikan pelayanan Informasi Publik yang cepat, tepat, dan sederhana;
								</li>
								<li>menyusun standar operasional prosedur pelaksanaan tugas dan kewenangan PPID Badan Kepegawaian Negara dalam rangka penyebarluasan Informasi Publik;
								</li>
								<li>menetapkan Daftar Informasi Publik dalam bentuk keputusan PPID Badan Kepegawaian Negara mengenai Daftar Informasi Publik Badan Kepegawaian Negara;
								</li>
								<li>melaksanakan Pengklasifikasian Informasi Publik atau perubahannya dengan persetujuan Atasan PPID Badan Kepegawaian Negara dalam bentuk keputusan PPID Badan Kepegawaian Negara mengenai klasifikasi informasi Badan Kepegawaian Negara;
								</li>
								<li>menetapkan Informasi Publik yang Dikecualikan sebagai Informasi Publik yang dapat diakses dalam hal:   
									<ol type="1">
										<li>telah dinyatakan terbuka bagi masyarakat berdasarkan mekanisme keberatan;
										</li>
										<li>telah dinyatakan terbuka bagi masyarakat berdasarkan putusan sidang ajudikasi, putusan pengadilan, serta putusan Mahkamah Agung;
										</li>
										<li>telah habis jangka waktu pengecualiannya; dan/atau
										</li>
										<li>ditentukan oleh peraturan perundang-undangan;
										</li>
									</ol>
								</li>
								<li>menetapkan pertimbangan tertulis atas setiap kebijakan yang diambil guna memenuhi hak setiap orang atas Informasi Publik;
								</li>
								<li>mengoordinasikan</li>
							</ol>
							<br>
							<ol type="1">
								<li>pengumpulan seluruh Informasi Publik yang meliputi:
								</li>
								<ol>
									<li>informasi yang wajib disediakan dan diumumkan secara berkala;
									</li>
									<li>informasi yang wajib diumumkan secara serta-merta; dan
									</li>
									<li>informasi yang wajib tersedia setiap saat;
									</li>
								</ol>
								<li>pengumpulan Informasi Publik yang Dikecualikan;
								</li>
								<li>pengumuman Informasi Publik melalui media yang secara efektif dan efisien dapat menjangkau seluruh pemangku kepentingan;
								</li>
								<li>penyampaian Informasi Publik dalam bahasa Indonesia yang baik, benar, dan mudah dipahami;
								</li>
								<li>pemenuhan permohonan Informasi Publik yang dapat diakses oleh publik;
								</li>
								<li>Pengklasifikasian Informasi Publik dan/atau pengubahan Pengklasifikasian Informasi Publik;
								</li>
								<li>permohonan keberatan diproses berdasarkan prosedur; dan
								</li>
								<li>proses pemberian Informasi Publik di Badan Kepegawaian Negara berjalan dengan baik;
								</li>
							</ol>
		
							<br>
		
							<ol type="1">
								<li>melakukan Uji Konsekuensi bersama PIS PPID unit terkait terhadap Informasi Publik yang tidak dapat diakses oleh masyarakat sebagaimana dimaksud dalam peraturan perundang-undangan mengenai keterbukaan Informasi Publik;
								</li>
								<li>memberikan alasan tertulis atas pengecualian Informasi Publik, dalam hal permohonan Informasi Publik ditolak;
								</li>
								<li>melakukan penghitaman atau pengaburan materi Informasi Publik yang Dikecualikan beserta alasannya;
								</li>
								<li>menetapkan dan menugaskan petugas layanan informasi untuk membantu pelaksanaan tugas PPID Badan Kepegawaian Negara;
								</li>
								<li>melakukan pengembangan kompetensi petugas layanan informasi guna meningkatkan kualitas layanan Informasi Publik;
								</li>
								<li>menggunakan Sistem Informasi PPID dalam pengelolaan layanan Informasi Publik;
								</li>
								<li>menyediakan Informasi Publik yang mutakhir pada portal Badan Kepegawaian Negara dan portal PPID;
								</li>
								<li>memelihara dan/atau memutakhirkan informasi pada portal Badan Kepegawaian Negara dan portal PPID paling kurang 1 (satu) kali dalam 1 (satu) bulan;
								</li>
								<li>melakukan koordinasi, harmonisasi, dan fasilitasi Perangkat PPID;
								</li>
								<li>menyediakan ruangan dan/atau meja layanan Informasi Publik:
								</li>
								<li>membuat dan menyampaikan laporan empat bulanan layanan Informasi Publik kepada Atasan PPID Badan Kepegawaian Negara; dan
								</li>
								<li>membuat dan mengumumkan laporan tahunan layanan Informasi Publik serta menyampaikan salinannya kepada Komisi Informasi Pusat.</li>
							</ol>
						</p>
		
						<br>
		
						<h2>Fungsi</h2>
						<P>Pembinaan dan pengelolaan Pejabat Pengelola Informasi dan Dokumentasi di lingkungan Badan Kepegawaian Negara.</P>
						<br>
		
						<h2>Wewenang</h2>
						<P>Dalam melaksanakan tugas, PPID Badan Kepegawaian Negara berwenang:</P>
						<p>
							<ol type="1">
								<li>memutuskan suatu informasi dapat diakses atau tidak dapat diakses oleh masyarakat berdasarkan Uji Konsekuensi bersama PIS PPID unit terkait;
								</li>
								<li>menolak permohonan Informasi Publik secara tertulis apabila Informasi Publik yang dimohonkan termasuk Informasi Publik yang Dikecualikan dengan disertai alasan serta pemberitahuan tentang hak dan tata cara bagi Pemohon untuk mengajukan keberatan atas penolakan tersebut;
								</li>
								<li>menghadiri rapat pembahasan terkait PPID di tingkat kementerian/lembaga;
								</li>
								<li>meminta informasi kepada Perangkat PPID pemilik informasi dalam hal Informasi Publik yang dimohonkan oleh Pemohon tidak dikuasai oleh PPID Badan Kepegawaian Negara namun dikuasai oleh Perangkat PPID;
								</li>
								<li>melakukan koordinasi dengan Perangkat PPID dan/atau unit terkait dalam menyelesaikan keberatan;
								</li>
								<li>melakukan pendampingan dan koordinasi dengan Perangkat PPID, unit teknis, dan/atau unit yang memiliki tugas dan fungsi memberikan bantuan hukum, pendapat hukum, dan pertimbangan hukum yang berkaitan dengan tugas Badan Kepegawaian Negara;
								</li>
								<li>mengusulkan kepada Atasan PPID Badan Kepegawaian Negara untuk melaporkan dan/atau mengajukan gugatan atas putusan Komisi Informasi ke lembaga peradilan;
								</li>
								<li>melakukan koordinasi dengan Perangkat PPID dalam penyediaan Informasi Publik yang mutakhir pada portal Badan Kepegawaian Negara dan situs selain portal Badan Kepegawaian Negara, dan/atau Sistem Informasi PPID;
								</li>
								<li>melaporkan ketidaksesuaian proses sidang Sengketa Informasi Publik ke Sekretariat Komisi Informasi atas persetujuan Atasan PPID Badan Kepegawaian Negara; dan
								</li>
								<li>melakukan sosialisasi untuk meningkatkan pemahaman atas implementasi keterbukaan Informasi Publik di Badan Kepegawaian Negara.
								</li>
							</ol>
						</p>
		
